def is_fundamental(name: str, profile: str):
    """
    Определяет, является ли дисциплина фундаментальной для профиля.
    """
    name = name.lower()

    keys = PROFILE_FUNDAMENTALS.get(profile, [])

    if any(k in name for k in keys):
        return True

    soft = [
        "культур", "истор", "философ", "психолог",
        "коммуник", "soft", "самоменедж", "управление временем",
        "риторик", "социолог"
    ]
    if any(k in name for k in soft):
        return False

    return False

def generate_disciplines(profile, df_fgos=None, tf_struct=None, fgos_text="", min_fund=12, min_var=15):
    """
    Генерация фундаментальных и вариативных дисциплин под профиль.
    Учитывает компетенции ФГОС и трудовые функции профстандарта.
    """
    import pandas as pd
    
    # Подготовка контекста для генерации
    competencies_info = ""
    if df_fgos is not None and not df_fgos.empty:
        comp_codes = df_fgos["code"].tolist()[:20]  # Первые 20 компетенций
        competencies_info = f"\nКомпетенции ФГОС, которые должны формироваться: {', '.join(comp_codes)}"
    
    tf_info = ""
    if tf_struct and tf_struct.get("TF"):
        tf_codes = [tf.get("code", "") for tf in tf_struct["TF"][:10]]
        tf_info = f"\nТрудовые функции профстандарта: {', '.join(tf_codes)}"
    
    # Контекст для разных профилей
    profile_contexts = {
        "педагогика": "\nВАЖНО: Это педагогический профиль! Дисциплины должны включать педагогику, психологию, методику обучения, образовательные технологии, возрастную психологию, теорию обучения.",
        "ивт": "\nПрофиль: Информатика и вычислительная техника. Фокус на программировании, алгоритмах, базах данных, архитектуре систем, операционных системах.",
        "информатик": "\nПрофиль: Информатика и вычислительная техника. Фокус на программировании, алгоритмах, базах данных, архитектуре систем.",
        "экономика": "\nПрофиль: Экономика. Дисциплины должны включать экономическую теорию, микроэкономику, макроэкономику, бухгалтерский учет, финансы, статистику.",
        "юриспруденция": "\nПрофиль: Юриспруденция. Дисциплины должны включать теорию государства и права, конституционное право, гражданское право, уголовное право, административное право.",
        "психология": "\nПрофиль: Психология. Дисциплины должны включать общую психологию, возрастную психологию, социальную психологию, психодиагностику, психологию личности.",
        "менеджмент": "\nПрофиль: Менеджмент. Дисциплины должны включать теорию менеджмента, управление персоналом, маркетинг, управление проектами, стратегический менеджмент.",
        "дизайн": "\nПрофиль: Дизайн. Дисциплины должны включать основы дизайна, композицию, цветоведение, компьютерную графику, типографику.",
    }
    
    profile_lower = profile.lower()
    profile_context = ""
    for key, context in profile_contexts.items():
        if key in profile_lower:
            profile_context = context
            break
    
    if not profile_context:
        profile_context = f"\nПрофиль: {profile}. Генерируй дисциплины, соответствующие данному профилю подготовки."

    prompt = f"""
Ты — опытный методист вуза РФ с 15+ годами опыта.

Профиль подготовки: "{profile}"
{profile_context}

{competencies_info}
{tf_info}

ТВОЯ ЗАДАЧА: Сгенерировать РЕАЛИСТИЧНЫЙ набор дисциплин для учебного плана.

ПРАВИЛА:
1. ФУНДАМЕНТАЛЬНЫЕ дисциплины — обязательные базовые дисциплины профиля
2. ВАРИАТИВНЫЕ дисциплины — должны СООТВЕТСТВОВАТЬ профилю и компетенциям
3. НЕ придумывай фантастические дисциплины (типа "криптовалюты", "VR в медицине" без контекста)
4. Дисциплины должны быть РЕАЛЬНЫМИ и ПРИМЕНИМЫМИ
5. Если профиль педагогический — обязательно включи педагогику, психологию, методику
6. Если профиль ИТ — фокус на программирование, алгоритмы, системы

Сгенерируй:
- минимум {min_fund} фундаментальных дисциплин
- минимум {min_var} вариативных дисциплин (реалистичных, соответствующих профилю!)

Верни строго JSON:
{{
  "fundamental": [{{"name": "Название"}}],
  "variative": [{{"name": "Название"}}]
}}
"""

    raw = call_yandex_lite(
        [{"role": "user", "text": prompt}],
        temperature=0.15,  # Снижена температура для более детерминированных результатов
        max_tokens=3000
    )

    try:
        start = raw.index("{")
        end = raw.rindex("}") + 1
        data = json.loads(raw[start:end])
    except Exception as e:
        raise RuntimeError(f"Ошибка генерации дисциплин: {e}") from e

    fund = data.get("fundamental", [])
    var = data.get("variative", [])

    # Фильтрация нереалистичных дисциплин
    filtered_fund = []
    filtered_var = []
    
    # Список нежелательных слов
    bad_keywords = ["криптовалют", "блокчейн в финансах", "vr в медицине", "умных домов", "умных городов"]
    
    for d in fund:
        name = d.get("name", "").lower()
        if not any(bad in name for bad in bad_keywords):
            filtered_fund.append(d)
    
    for d in var:
        name = d.get("name", "").lower()
        if not any(bad in name for bad in bad_keywords):
            filtered_var.append(d)

    for d in filtered_fund:
        d["block_hint"] = "обязательная"
    for d in filtered_var:
        d["block_hint"] = "вариативная"

    return filtered_fund + filtered_var
