def detect_profile_type(profile: str) -> str:
    """
    Определяет тип направления.
    Это нужно, чтобы модель не переносила педагогические дисциплины
    в технические, художественные или физико-математические планы.
    """
    p = (profile or "").lower()

    if any(x in p for x in [
        "информатика",
        "вычислительная техника",
        "информационные технологии",
        "программ",
        "ит",
        "09.03",
        "09.04"
    ]):
        return "technical"

    if any(x in p for x in [
        "педагог",
        "образование",
        "учитель",
        "44.03",
        "44.04"
    ]):
        return "pedagogical"

    if any(x in p for x in [
        "живопись",
        "искусств",
        "дизайн",
        "художе",
        "54.05",
        "50.03"
    ]):
        return "art"

    if any(x in p for x in [
        "математика",
        "физика",
        "прикладные математика и физика",
        "03.04",
        "03.03"
    ]):
        return "science"

    return "generic"


def get_profile_fundamental_key(profile: str) -> str:
    """
    Подбирает ключ для PROFILE_FUNDAMENTALS по строке профиля.
    """
    p = (profile or "").lower()

    if any(x in p for x in ["информатика", "вычислительная техника", "ит", "09.03", "09.04"]):
        return "Информационные технологии"

    if any(x in p for x in ["педагог", "образование", "44.03", "44.04"]):
        return "Педагогика"

    if any(x in p for x in ["живопись", "искусств", "художе", "54.05", "50.03"]):
        return "Живопись"

    if any(x in p for x in ["математика", "физика", "03.04", "03.03"]):
        return "Прикладные математика и физика"

    if "эконом" in p:
        return "Экономика"

    if "юрис" in p or "прав" in p:
        return "Юриспруденция"

    if "психолог" in p:
        return "Психология"

    if "менедж" in p:
        return "Менеджмент"

    if "дизайн" in p:
        return "Дизайн"

    return profile


def is_fundamental(name: str, profile: str):
    """
    Определяет, является ли дисциплина фундаментальной для профиля.
    """
    name = (name or "").lower()
    profile_key = get_profile_fundamental_key(profile)

    keys = PROFILE_FUNDAMENTALS.get(profile_key, [])

    if any(k in name for k in keys):
        return True

    soft = [
        "культур", "истор", "философ", "психолог",
        "коммуник", "soft", "самоменедж", "управление временем",
        "риторик", "социолог"
    ]

    if any(k in name for k in soft):
        return False

    return False


def _safe_get_competencies(df_fgos):
    """
    Достаёт коды компетенций из DataFrame без падения программы.
    """
    if df_fgos is None:
        return ""

    try:
        if df_fgos.empty:
            return ""

        if "code" not in df_fgos.columns:
            return ""

        comp_codes = [
            str(x).strip()
            for x in df_fgos["code"].tolist()
            if str(x).strip()
        ]

        return ", ".join(comp_codes[:25])

    except Exception:
        return ""


def _safe_get_tf(tf_struct):
    """
    Достаёт трудовые функции из профстандарта без падения программы.
    """
    if not tf_struct or not tf_struct.get("TF"):
        return ""

    result = []

    try:
        for tf in tf_struct["TF"][:15]:
            code = str(tf.get("code", "")).strip()
            name = str(tf.get("name", "")).strip()

            if code and name:
                result.append(f"{code}: {name}")
            elif code:
                result.append(code)
            elif name:
                result.append(name)

    except Exception:
        return ""

    return "; ".join(result)


def _is_bad_discipline(name: str, forbidden_words):
    """
    Проверяет, нельзя ли использовать дисциплину для данного профиля.
    """
    low = (name or "").lower()

    if not low:
        return True

    if any(bad in low for bad in BAD_KEYWORDS_COMMON):
        return True

    if any(word.lower() in low for word in forbidden_words):
        return True

    return False


def _normalize_discipline(item):
    """
    Приводит дисциплину к единому виду.
    """
    if isinstance(item, str):
        return {"name": item.strip()}

    if isinstance(item, dict):
        name = (
            item.get("name")
            or item.get("discipline")
            or item.get("title")
            or ""
        )

        return {"name": str(name).strip()}

    return {"name": ""}


def _filter_disciplines(items, forbidden_words):
    """
    Удаляет дисциплины, которые не подходят профилю.
    """
    filtered = []

    for item in items:
        d = _normalize_discipline(item)
        name = d.get("name", "")

        if _is_bad_discipline(name, forbidden_words):
            continue

        filtered.append(d)

    return filtered


