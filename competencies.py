def detect_competencies(profile: str, discipline_name: str) -> list:
    profile_matrix = COMPETENCY_MATRIX.get(profile)
    if not profile_matrix:
        return []

    comps = profile_matrix.get(discipline_name)
    if not comps:
        return []

    return list(comps)
